package org.example;

public class operators {
    public static void main() {
        int a =10 ;
        int b = 2;
        System.out.println(a*b);
        System.out.println(a / b);
        System.out.println(a-b);
        System.out.println(a+b);
        System.out.println(a % b);

        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(++a);

        System.out.println(a>b && b>a);
        System.out.println(a>b || b>a);

        a +=5;
        System.out.println(a);

        a-=8;
        System.out.println(a);

        a *=2;
        System.out.println(a);

        a /=2;
        System.out.println(a);



    }
}
